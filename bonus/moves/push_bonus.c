#include "../checker_bonus.h"

void	push(t_list **a, t_list **b)
{
	t_list	*tmp;

	if (!a || !(*b))
		return ;
	tmp = (*b)->next;
	(*b)->next = *a;
	*a = *b;
	*b = tmp;
}